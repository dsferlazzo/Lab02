package it.polito.alien.model;

public class Parola {
	
	String pAliena;
	String pUmana;
	
	
	public Parola(String pAliena, String pUmana) {
		super();
		this.pAliena = pAliena;
		this.pUmana = pUmana;
	}


	public String getpAliena() {
		return pAliena;
	}



	public String getpUmana() {
		return pUmana;
	}


	
	

}
